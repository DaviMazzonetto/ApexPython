print('Olá, Apex')
nome = input('Digite o nome:')

# Python é dinamicamente tipado, a variável define o tipo assim que recebe o valor.
# type retorna o tipo dá variavel.
# id retorna o identificador que é baseado no end de memória.

print(type(nome)) # str
print(id(nome)) 
nome = 12
print(type(nome)) # int
print(id(nome))
nome = 12.2
print(type(nome)) # float
print(id(nome))
nome = True
print(type(nome)) # bool
print(id(nome))
numero = int('int')
print(type(numero)) # int

# int, str, float, bool






