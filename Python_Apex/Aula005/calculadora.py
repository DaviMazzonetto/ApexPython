def soma(n1,n2,n3=0,n4=0): #Parametros
    return n1 + n2 + n3 + n4


def subt(n1,n2,n3=0,n4=0): #Parametros
    return n1 - n2 - n3 - n4

def mult(n1,n2,n3=1,n4=1): #Parametros
    return n1 * n2 * n3 * n4

def div(n1,n2,n3,n4): # Parametros
    return n1 / n2/ n3 / n4

